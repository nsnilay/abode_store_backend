package com.example.Product.dto;

public class Product {
    private String pId;
    private String pDescription;
    private String pName;
    private String pCatagory;
    private String pImageUrl;
    private String pUsp;
    private String pStock;
    private String pAttributes;
    private String pPrice;

    public String getpId() {
        return pId;
    }

    public void setpId(String pId) {
        this.pId = pId;
    }

    public String getpDescription() {
        return pDescription;
    }

    public void setpDescription(String pDescription) {
        this.pDescription = pDescription;
    }

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }

    public String getpCatagory() {
        return pCatagory;
    }

    public void setpCatagory(String pCatagory) {
        this.pCatagory = pCatagory;
    }

    public String getpUsp() {
        return pUsp;
    }

    public void setpUsp(String pUsp) {
        this.pUsp = pUsp;
    }

    public String getpStock() {
        return pStock;
    }

    public void setpStock(String pStock) {
        this.pStock = pStock;
    }

    public String getpAttributes() {
        return pAttributes;
    }

    public void setpAttributes(String pAttributes) {
        this.pAttributes = pAttributes;
    }

    public String getpPrice() {
        return pPrice;
    }

    public void setpPrice(String pPrice) {
        this.pPrice = pPrice;
    }

    public String getpImageUrl() {
        return pImageUrl;
    }

    public void setpImageUrl(String pImageUrl) {
        this.pImageUrl = pImageUrl;
    }
}
